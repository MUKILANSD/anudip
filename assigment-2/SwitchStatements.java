
 public class Switch {
	  public static void main(String[] args) {

		    int Distance = 2;

		    switch (Distance) {
		      case 1:
		        System.out.println("Near");

		      case 2:
		        System.out.println("Fall");

		      default:
		        System.out.println("No Distance");
		    }
		  }
}